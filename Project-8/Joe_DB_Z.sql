CREATE PROCEDURE GetCustomerInvoiceDetails(IN customerNumber INT)
BEGIN
    SELECT
    INVOICE.CUS_NUM AS CustomerNumber,
    INVOICE.INV_NUM AS InvoiceNumber,
    INVOICE.INV_DATE AS InvoiceDate,
    LINE.LINE_UNITS AS Quantity,
    LINE.LINE_PRICE AS UnitPrice,
    (LINE.LINE_UNITS * LINE.LINE_PRICE) AS TotalPrice
    FROM INVOICE
    JOIN LINE ON INVOICE.INV_NUM = LINE.INV_NUM
    WHERE INVOICE.CUS_NUM = customerNumber;
END;
